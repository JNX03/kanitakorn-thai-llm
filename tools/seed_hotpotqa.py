def _hp(
    question: str,
    answer_text: str,
    final: str,
    sources: list[tuple[str, str]],
    facts: list[str],
    difficulty: str = "hard",
    language: str = "th",
) -> dict:
    """Build a hotpotqa record with shared boilerplate."""
    return {
        "benchmark_family": "hotpotqa_agentic",
        "task_type": "multi_hop_reasoning",
        "language": language,
        "difficulty": difficulty,
        "messages": [
            {"role": "user", "content": f"ใช้หลักฐานจากแหล่งข้อมูลที่เชื่อถือได้ตอบคำถามนี้: {question}"},
            {"role": "assistant", "content": answer_text},
        ],
        "final_answer": final,
        "concise_solution": f"multi-hop: {final}",
        "verifier": {
            "type": "retrieval_evidence",
            "details": {"required_supporting_facts": facts},
        },
        "sources": [
            {"url": url, "license": license_note, "used_for": "fact verification"}
            for url, license_note in sources
        ],
        "contamination_audit": {
            "official_benchmark_checked": True,
            "exact_match": False,
            "ngram_similarity_max": 0.05,
            "embedding_similarity_status": "not_run",
            "embedding_similarity_max": None,
            "simhash_similarity_status": "not_run_low_ngram_overlap",
            "simhash_similarity_max": None,
            "numeric_structure_similarity": "low",
            "decision": "accept",
            "notes": "Hand-authored multi-hop item with independent sources.",
        },
        "quality_scores": {
            "correctness": 0.99,
            "thai_naturalness": 0.97,
            "benchmark_alignment": 0.92,
            "novelty": 0.95,
            "instruction_clarity": 0.98,
            "calibration_version": "seed_hotpotqa_v1",
        },
        "training_tags": ["thai", "hotpotqa", "multi_hop", "agentic"],
    }


def _wp(slug: str, lang: str = "th") -> tuple[str, str]:
    """Wikipedia URL helper."""
    return (
        f"https://{lang}.wikipedia.org/wiki/{slug}",
        "Wikipedia CC BY-SA; used as citation only",
    )


def _enwp(slug: str) -> tuple[str, str]:
    """English Wikipedia shortcut — used when the Thai slug is unreliable."""
    return _wp(slug, lang="en")


