def main() -> int:
    out_path = ROOT / "dataset" / "train" / "train_hotpotqa_agentic_seed.jsonl"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    # Unlock target file if it was read-only-locked.
    if sys.platform == "win32" and out_path.exists():
        subprocess.run(["attrib", "-R", str(out_path)], check=False, capture_output=True)
    # Also clear any pre-existing val_hotpotqa_*.jsonl seed records that
    # would duplicate the seeds we're about to write.
    val_dir = ROOT / "dataset" / "validation"
    if val_dir.exists():
        for p in val_dir.glob("val_hotpotqa_agentic_*.jsonl"):
            # Read records and drop any seed-ids; rewrite without them.
            try:
                if sys.platform == "win32":
                    subprocess.run(["attrib", "-R", str(p)], check=False, capture_output=True)
                lines = p.read_text(encoding="utf-8").splitlines()
                kept = [l for l in lines if l.strip() and "hotpotqa-agentic-th-seed-" not in l]
                if not kept:
                    p.unlink()
                else:
                    p.write_text("\n".join(kept) + "\n", encoding="utf-8")
            except Exception:
                pass

    validator = Draft202012Validator(SCHEMA)
    accepted = 0
    rejected = []
    with out_path.open("w", encoding="utf-8") as fh:
        for i, seed in enumerate(SEEDS):
            seed["id"] = f"hotpotqa-agentic-th-seed-{i:04d}"
            seed["task_type"] = seed.get("task_type", "multi_hop_reasoning")
            try:
                validator.validate(seed)
            except Exception as e:
                rejected.append((i, str(e)[:200]))
                continue
            fh.write(json.dumps(seed, ensure_ascii=False) + "\n")
            accepted += 1
    if sys.platform == "win32":
        subprocess.run(["attrib", "+R", str(out_path)], check=False, capture_output=True)
    print(f"wrote {accepted}/{len(SEEDS)} hotpotqa seeds → {out_path}")
    for idx, msg in rejected:
        print(f"  REJECTED {idx}: {msg}")
    return 0 if not rejected else 1


if __name__ == "__main__":
    raise SystemExit(main())
