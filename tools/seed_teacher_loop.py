def main() -> int:
    out_path = ROOT / "dataset" / "train" / "train_teacher_loop_th_000.jsonl"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    import subprocess as _sp
    # Unlock train file if it was read-only-locked.
    if sys.platform == "win32" and out_path.exists():
        _sp.run(["attrib", "-R", str(out_path)], check=False, capture_output=True)
    # Clear any pre-existing val_teacher_loop_th_*.jsonl files so rebalance
    # doesn't end up with duplicate ids in train + val.
    val_dir = ROOT / "dataset" / "validation"
    if val_dir.exists():
        for p in val_dir.glob("val_teacher_loop_th_*.jsonl"):
            try:
                p.unlink()
            except PermissionError:
                if sys.platform == "win32":
                    _sp.run(["attrib", "-R", str(p)], check=False, capture_output=True)
                    p.unlink()
    # Also unlock any pre-existing train_teacher_loop_th_*.jsonl > 000 (rebalance may have created shards).
    # Skip files that are locked by another process (e.g., still being written to);
    # the rebalance step dedupes by id so duplicates aren't a problem.
    train_dir = ROOT / "dataset" / "train"
    for p in train_dir.glob("train_teacher_loop_th_*.jsonl"):
        if p.name == out_path.name:
            continue
        try:
            p.unlink()
        except (PermissionError, OSError) as _e:
            if sys.platform == "win32":
                _sp.run(["attrib", "-R", str(p)], check=False, capture_output=True)
                try:
                    p.unlink()
                except (PermissionError, OSError):
                    pass  # leave it; rebalance dedupe handles it

    validator = Draft202012Validator(SCHEMA)
    accepted = 0
    with out_path.open("w", encoding="utf-8") as fh:
        for i, seed in enumerate(SEED_RECORDS):
            rec = LoopRecord(
                skill=seed["skill"],
                base_family=seed["base_family"],
                messages=seed["messages"],
                n_corrections=seed["n_corrections"],
                convergence_reached=seed["convergence"],
                init_prompt=seed["messages"][0]["content"],
            )
            packed = record_to_schema(rec, f"teacher-loop-th-seed-{i:04d}")
            try:
                validator.validate(packed)
            except Exception as e:
                print(f"  REJECTED seed {i}: {e}")
                continue
            fh.write(json.dumps(packed, ensure_ascii=False) + "\n")
            accepted += 1
    print(f"wrote {accepted}/{len(SEED_RECORDS)} seed records → {out_path}")
    return 0 if accepted == len(SEED_RECORDS) else 1


if __name__ == "__main__":
    raise SystemExit(main())
